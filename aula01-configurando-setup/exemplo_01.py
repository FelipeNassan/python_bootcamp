# A função print() imprime textos, ou seja, mostra ao usuário o conteúdo que quisermos
print("Hello, World!")
print("Qualquer coisa")
print(3)
print(3 + 4)
print("Olá" + ", " + "turma!")

# A função input() serve para o usuário inserir dados
input("Digite seu nome: ")
print(input("Digite seu nome: "))
print("Olá, " + input("Digite seu nome: ") + "!")